function construirBDIdioma(productos, arrayIdio){

    let prodtos=cargarDelLocalStorage()
    if(prodtos.length !=0){
        for(let i=0; i< prodtos.length; i++){
            prodtos[i].description=arrayIdio[i].description
            prodtos[i].name=arrayIdio[i].name
        }
    }
    else{
        prodtos=productos
    }
    
    return prodtos
}

function cargarBD(idiomaSelect){
    
    let prodIdioma=[] 

    idiomaSelect="Español" //por ahora solo tenemos base de datos en español

    if(idiomaSelect=="Español"){
        prodIdioma=construirBDIdioma(productos, productos)
    }
    else if(idiomaSelect=="Inglés"){
        prodIdioma=construirBDIdioma(productos, ingles)
    }
    
    cargarLocalStorage(prodIdioma)
}


function cargarLocalStorage(productos){

    let aux;
    for(let i=0; i<productos.length;i++){
        
        aux = JSON.stringify(productos[i])
        localStorage.setItem( i, aux)


    }

}


function cargarDelLocalStorage(){

    let aux;
    let prtos =[];
    for(let i=0; i<productos.length;i++){
  
        aux= localStorage.getItem(i)
        aux != null ? prtos.push(JSON.parse(aux)) : i=productos.length;
        //prtos.push(aux)
    }
  
    return prtos;
  
}